static void print_buf(const char* buf_name, const void* buf, size_t length)
{
	const uint8_t* ptr = buf;
	printf("%s: ", buf_name);
	for (size_t i = 0; i < length; i++) {
		printf("%02X", ptr[i]);
	}
	printf("\n");
}

static int populate_tlv_list(
	const struct emv_tlv_t* tlv_array,
	size_t tlv_array_count,
	struct emv_tlv_list_t* list
)
{
	int r;

	emv_tlv_list_clear(list);
	for (size_t i = 0; i < tlv_array_count; ++i) {
		r = emv_tlv_list_push(list, tlv_array[i].tag, tlv_array[i].length, tlv_array[i].value, 0);
		if (r) {
			return r;
		}
	}

	return 0;
}

int main(void)
{
	int r;
	const struct emv_capk_t* capk;
	struct emv_tlv_list_t invalid_icc = EMV_TLV_LIST_INIT;
	struct emv_tlv_list_t valid_icc = EMV_TLV_LIST_INIT;
	struct emv_rsa_issuer_pkey_t ipk;

	r = emv_capk_init();
	if (r) {
		fprintf(stderr, "emv_capk_init() failed; r=%d\n", r);
		r = 1;
		goto exit;
	}
	capk = emv_capk_lookup(test_capk_rid, test_capk_index);
	if (!capk) {
		fprintf(stderr, "emv_capk_lookup() failed\n");
		r = 1;
		goto exit;
	}

	// Populate fields required for issuer public key validation
	r = populate_tlv_list(
		invalid_icc_data,
		sizeof(invalid_icc_data) / sizeof(invalid_icc_data[0]),
		&invalid_icc
	);
	if (r) {
		fprintf(stderr, "populate_tlv_list() failed; r=%d\n", r);
		r = 1;
		goto exit;
	}

	// Populate fields required for issuer public key validation
	r = populate_tlv_list(
		valid_icc_data,
		sizeof(valid_icc_data) / sizeof(valid_icc_data[0]),
		&valid_icc
	);
	if (r) {
		fprintf(stderr, "populate_tlv_list() failed; r=%d\n", r);
		r = 1;
		goto exit;
	}

	// Test failed retrieval of issuer public key
	memset(&ipk, 0, sizeof(ipk));
	r = emv_rsa_retrieve_issuer_pkey(
		invalid_issuer_cert,
		sizeof(invalid_issuer_cert),
		capk,
		NULL,
		&ipk
	);
	if (r >= 0) {
		fprintf(stderr, "Unexpected emv_rsa_retrieve_issuer_pkey() result; r=%d\n", r);
		r = 1;
		goto exit;
	}

	// Test retrieval of issuer public key without validation
	memset(&ipk, 0, sizeof(ipk));
	r = emv_rsa_retrieve_issuer_pkey(
		valid_issuer_cert,
		sizeof(valid_issuer_cert),
		capk,
		NULL,
		&ipk
	);
	if (r != 1) {
		fprintf(stderr, "emv_rsa_retrieve_issuer_pkey() failed; r=%d\n", r);
		r = 1;
		goto exit;
	}
	if (memcmp(&ipk, &decrypted_issuer_pkey_verify, decrypted_issuer_pkey_verify_len) != 0) {
		fprintf(stderr, "Decrypted issuer public key is incorrect\n");
		print_buf("retrieved", &ipk, decrypted_issuer_pkey_verify_len);
		print_buf("expected", &decrypted_issuer_pkey_verify, decrypted_issuer_pkey_verify_len);
		r = 1;
		goto exit;
	}

	// Test retrieval of issuer public key with failed validation
	memset(&ipk, 0, sizeof(ipk));
	r = emv_rsa_retrieve_issuer_pkey(
		valid_issuer_cert,
		sizeof(valid_issuer_cert),
		capk,
		&invalid_icc,
		&ipk
	);
	if (r != 5) {
		fprintf(stderr, "emv_rsa_retrieve_issuer_pkey() failed; r=%d\n", r);
		r = 1;
		goto exit;
	}
	if (memcmp(&ipk, &decrypted_issuer_pkey_verify, decrypted_issuer_pkey_verify_len) != 0) {
		fprintf(stderr, "Decrypted issuer public key is incorrect\n");
		print_buf("retrieved", &ipk, decrypted_issuer_pkey_verify_len);
		print_buf("expected", &decrypted_issuer_pkey_verify, decrypted_issuer_pkey_verify_len);
		r = 1;
		goto exit;
	}

	// Test retrieval of issuer public key with validation
	memset(&ipk, 0, sizeof(ipk));
	r = emv_rsa_retrieve_issuer_pkey(
		valid_issuer_cert,
		sizeof(valid_issuer_cert),
		capk,
		&valid_icc,
		&ipk
	);
	if (r) {
		fprintf(stderr, "emv_rsa_retrieve_issuer_pkey() failed; r=%d\n", r);
		r = 1;
		goto exit;
	}
	if (memcmp(&ipk, &full_issuer_pkey_verify, sizeof(full_issuer_pkey_verify)) != 0) {
		fprintf(stderr, "Retrieved issuer public key is incorrect\n");
		print_buf("retrieved", &ipk, sizeof(ipk));
		print_buf("expected", &full_issuer_pkey_verify, sizeof(full_issuer_pkey_verify));
		r = 1;
		goto exit;
	}

	printf("Success\n");
	r = 0;
	goto exit;

exit:
	emv_tlv_list_clear(&invalid_icc);
	emv_tlv_list_clear(&valid_icc);
	return r;
}
